# -*- coding: utf-8 -*-
import csv
import codecs

def write_csv(filename, data):
    """UTF-8 BOM으로 CSV 파일 저장"""
    with codecs.open(filename, 'w', 'utf-8-sig') as f:
        writer = csv.writer(f, lineterminator='\n')
        writer.writerows(data)
    print(f"{filename} 생성 완료!")

